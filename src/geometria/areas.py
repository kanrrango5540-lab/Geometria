import math

def area_rectangle(base, height): 
    area_rectangle = base * height
    return area_rectangle
"""
calcula el area de un rectangulo
base: la base del rectangulo
height: la altura del rectangulo
return: el area del rectangulo
"""

def area_circle(radius):
    area_circle = math.pi * radius ** 2
    return area_circle
"""
calcula el area de un circulo
radius: el radio del circulo
return: el area del circulo
"""

def area_triangle(base, height):
    area_triangle = (base * height) / 2
    return area_triangle
"""
calcula el area de un triangulo
base: la base del triangulo
height: la altura del triangulo
return: el area del triangulo
"""
